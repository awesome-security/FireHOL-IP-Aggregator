    def __init__(self, meta, maintainer, maintainer_url, list_source_url, source_file_date, category, entries):
        self.meta = meta
        self.maintainer = maintainer
        self.maintainer_url = maintainer_url
        self.list_source_url = list_source_url
        self.source_file_date = source_file_date
        self.category = category
        self.entries = entries
